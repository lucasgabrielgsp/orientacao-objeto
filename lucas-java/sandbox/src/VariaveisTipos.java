public class VariaveisTipos {
    public static void main(String[] args) {
        // Tipos primitivos
        int idade = 10;
        double preco = 12.45;
        float altura = 1.80f;
        char sexo = 'M';
        boolean solteiro = true;

        // Tipos não primitivos
        Integer idade2 = 10;
        Double preco2 = 12.45;
        Float altura2 = 1.80f;
        Character sexo2 = 'M';
        Boolean solteiro2 = true;
        String nome = "Fulano";

        System.out.println(nome);
        System.out.println("Idade: " + idade);
        System.out.println("Preço: " + preco);
        System.out.println("Altura: " + altura);
        System.out.println("Sexo: " + sexo);
        System.out.println("Solteiro: " + solteiro);

        System.out.println("Idade: " + idade2);
        System.out.println("Preço: " + preco2);
        System.out.println("Altura: " + altura2);
        System.out.println("Sexo: " + sexo2);
        System.out.println("Solteiro: " + solteiro2);
    }

}
